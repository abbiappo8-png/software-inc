                check (status in ('running','completed','rolled_back','failed')),
  rows_ok       bigint not null default 0,
  rows_error    bigint not null default 0,
  started_at    text not null default to_char(now() at time zone 'utc', 'YYYY-MM-DD"T"HH24:MI:SS"Z"'),
