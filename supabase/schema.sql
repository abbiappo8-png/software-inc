values ('fotos', 'fotos', false), ('archivos', 'archivos', false)
on conflict (id) do nothing;

drop policy if exists fotos_archivos_authenticated on storage.objects;
create policy fotos_archivos_authenticated on storage.objects
  for all to authenticated
  using (bucket_id in ('fotos', 'archivos'))
  with check (bucket_id in ('fotos', 'archivos'));

