-- ---------- Vista de edades (equivalente a v_person_ages de SQLite) ----------
-- security_invoker: la vista respeta el RLS del usuario que consulta.

create or replace view v_person_ages
with (security_invoker = true) as
select id, full_name,
       extract(year from age(current_date, birth_date::date))::bigint as age
from persons
where birth_date is not null
  and birth_date ~ '^\d{4}-\d{2}-\d{2}$'
  and birth_date::date <= current_date;

-- =====================================================================
-- RLS: todo privado; solo `authenticated` (el usuario del PIN) entra.
-- =====================================================================

do $$
declare t text;
begin
  foreach t in array array[
    'settings','import_batches','import_errors','persons','service_catalog',
    'equipment','transactions','bar_products','bar_sales','expenses',
    'client_bills','client_bill_items','professor_settlements','settlement_payments',
    'payment_plans','payment_plan_installments','form_responses'
  ] loop
    execute format('alter table %I enable row level security', t);
    execute format('drop policy if exists todo_authenticated on %I', t);
    execute format(
      'create policy todo_authenticated on %I for all to authenticated using (true) with check (true)', t
    );
  end loop;
end $$;

