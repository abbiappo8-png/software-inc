-- ---------- Transacciones / reservas (Club) ----------
-- Incluye ya la migración 002 (tx_type, check_in_at) y la 003 (professor_salary
-- con REDONDEO half-up). En SQLite eran GENERATED VIRTUAL; aquí son STORED
-- (mismo resultado; Postgres las materializa).
--   * CAST(x + 0.5 AS INTEGER) de SQLite trunca tras sumar 0.5 = redondeo
--     half-up para valores >= 0  ->  floor(x + 0.5)::bigint en Postgres.
--     (No usamos round(): sobre double hace redondeo "bancario".)

create table if not exists transactions (
  id                     bigint generated by default as identity primary key,
  tx_date                text not null,
  start_min              bigint,
  end_min                bigint,
  service_raw            text,
  service_id             bigint references service_catalog(id),
  is_class               integer default 0 check (is_class in (0,1)),
  resolved_service_id    bigint references service_catalog(id),
  professor_id           bigint references persons(id),
  client_id              bigint references persons(id),
  kite_id                bigint references equipment(id),
  board_id               bigint references equipment(id),
  price_snapshot         bigint,
  professor_pct_snapshot double precision,
  price_override         bigint,
  tx_type                text not null default 'service'
                         check (tx_type in ('class','loan','service','other')),
  check_in_at            text,
  price_effective        bigint generated always as (coalesce(price_override, price_snapshot)) stored,
  duration_min           bigint generated always as (
                           case when end_min is not null and start_min is not null
                                then end_min - start_min end) stored,
  professor_salary       bigint generated always as (
                           floor((coalesce(price_override, price_snapshot)::numeric
                                  * coalesce(professor_pct_snapshot, 0)::numeric) + 0.5)::bigint
                         ) stored,
  comment                text,
  import_batch_id        bigint references import_batches(id),
  source_sheet           text,
  source_row             bigint,
  created_at             text not null default to_char(now() at time zone 'utc', 'YYYY-MM-DD"T"HH24:MI:SS"Z"')
);
create index if not exists ix_tx_date      on transactions(tx_date);
create index if not exists ix_tx_professor on transactions(professor_id, tx_date);
create index if not exists ix_tx_client    on transactions(client_id, tx_date);
create index if not exists ix_tx_service   on transactions(service_id);

